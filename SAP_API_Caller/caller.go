func (c *SAPAPICaller) getQueryWithHeader(params map[string]string, salesOrder string) map[string]string {
	if len(params) == 0 {
		params = make(map[string]string, 1)
	}
	params["$filter"] = fmt.Sprintf("SalesOrder eq '%s'", salesOrder)
	return params
}

func (c *SAPAPICaller) getQueryWithItem(params map[string]string, salesOrder, salesOrderItem string) map[string]string {
	if len(params) == 0 {
		params = make(map[string]string, 1)
	}
	params["$filter"] = fmt.Sprintf("SalesOrder eq '%s' and SalesOrderItem eq '%s'", salesOrder, salesOrderItem)
	return params
}
